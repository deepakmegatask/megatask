<script>
var words = ["Great things in business are never done by one person. They are done by a team of people.", "Our Team, Several Magic Hands Our customers always are treated by a committed manager who leads your firm to growth.", " A team of in-house professionals that bring their own magic to their journey supports the enthusiast. Their commitment is equal."],
  part,
  i = 0,
  offset = 0,
  len = words.length,
  forwards = true,
  skip_count = 0,
  skip_delay = 15,
  speed = 70;
var wordflick = function() {
  setInterval(function() {
    if(forwards) {
      if(offset >= words[i].length) {
        ++skip_count;
        if(skip_count == skip_delay) {
          forwards = false;
          skip_count = 0;
        }
      }
    } else {
      if(offset == 0) {
        forwards = true;
        i++;
        offset = 0;
        if(i >= len) {
          i = 0;
        }
      }
    }
    part = words[i].substr(0, offset);
    if(skip_count == 0) {
      if(forwards) {
        offset++;
      } else {
        offset--;
      }
    }
    $('.wordH').text(part);
  }, speed);
};
$(document).ready(function() {
  wordflick();
  $('.panel-group').on('hidden.bs.collapse', toggleIcon);
  $('.panel-group').on('shown.bs.collapse', toggleIcon);
});

function toggleIcon(e) {
  $(e.target).prev('.panel-heading').find(".more-less").toggleClass('fa fa-minus');
}
</script>